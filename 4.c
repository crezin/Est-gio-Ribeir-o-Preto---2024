#include <stdio.h>

int main() {
    // Suponha que os interruptores sejam numerados de 1 a 3
    printf("Ligue o interruptor 1 e deixe-o ligado por alguns minutos.\n");
    printf("Em seguida, desligue o interruptor 1 e ligue o interruptor 2.\n");
    printf("Agora vá até a sala das lâmpadas e veja qual está acesa e qual está apagada.\n");
    printf("O interruptor que foi ligado por mais tempo está conectado à lâmpada acesa,\n");
    printf("o que foi ligado por menos tempo está conectado à lâmpada apagada,\n");
    printf("e o que não foi tocado está conectado à lâmpada restante.\n");
    return 0;
}
